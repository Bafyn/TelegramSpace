        <h4>Как продвигали Telegram:</h4>
        <p>
            Павел Дуров, что называется, угадал самую насущную потребность людей в общении – сохранение
            конфиденциальности. Уж слишком вездесущими стали спецслужбы. Даже появился такой анекдот: «Большинство
            англичан опасаются, что их разговоры и переписку контролируют спецслужбы. Это стало известно на основе
            анализа разговор и переписки граждан Великобритании».<br>
            А тут как нельзя кстати разразился скандал со Сноуденом, который в июне 2013 года (то есть буквально за 2
            месяца перед появлением Telegram) рассказал о глобальном электронном колпаке США. Случайно совпадение по
            датам? Ну, допустим…<br>
            Но, как бы там ни было, Telegram с его знаменитым рекламным слоганом «Taking back our right to privacy»
            («Вернём себе право на приватность») оказался, что называется, в центре мейнстрима.<Br>
            Для усиления эффекта Павел Дуров объявил конкурс с 200 000 долларов призовых. Его должен был получить тот,
            кто смог бы прочитать переписку Павла с его братом Николаем в секретном чате. Какую-то уязвимость один
            парень нашел, но до самой переписке добраться так и не смог. За что и получил 100 000 долларов.<br>
            А по-настоящему раскручивать мессенджер стали с… Саудовской Аравии. Популярный арабский блогер Халед написал
            несколько постов о Telegram, которому ему якобы рекомендовал пользователь Твиттер @AboMayar. Количество
            скачиваний Telegram в App Store сразу же взметнулось до отметки 100 000. А потом вообще последовали события
            из ряда чудес . Влиятельные арабские телеканалы «Аль-Джазиры» и «Аль-Арабии» неожиданно порекомендовали
            своим сотрудникам перейти в общении на Telegram и завели там свои каналы!<br>Почему именно Саудовская
            Аравия? Нам кажется, тут сразу сошлось несколько факторов:<br>
            1. Саудовская Аравия уже давно с тревогой наблюдает, как США, вроде бы как союзник, баламутят Ближний
            Восток, пытаясь в мутной водичке поймать свою золотую рыбку. А зная принцип англосаксов, что у них нет
            постоянных друзей, а есть постоянные интересы, саудиты резонно полагают, что как бы в этом арабском шторме
            самим не попасть на рифы. А в современном мире, кто владеет информацией, тот владеет всем. Так почему бы эту
            информацию своим союзникам не урезать, перейдя на Telegram? Ведь все остальное – Gmail, Facebook, Skype и
            другие средства коммуникации давно находятся под плотной опекой США.<br>
            2. У Павла Дурова в Саудовской Аравии оказался влиятельный знакомый, некто Васильев, ранее возглавлявший
            совет директоров украинского консорциума ЕДАПС, выпускавший защищенные паспорта не только для Украины, но и
            для стран Персидского залива. В декабре 2013 года, уже после начала раскрутки Telegram в Саудовской Аравии и
            ОАЭ Дуров и Васильев встретятся в Петербурге, чтобы вместе отметить Новый Год. Встреча прошла на высшем
            уровне – в ресторане «Терраса» недалеко от Исаакиевского собора в компании обольстительных девушек-моделей,
            которых организовал фотограф Александр Маврин, один из лучших друзей Павла Дурова. И еще интересный факт. В
            2015 году Верховный Суд США обвинил ЕДАПС в финансовых злоупотреблениях и Васильев был экстрадирован в США.
            Так что не исключено, что сейчас у американцев и на Павла Дурова есть компромат.
