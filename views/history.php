            Уверены, что когда-то на основе истории создания мессенджера Telegram напишут хороший детективный
            бестселлер, не хуже «Кода да Винчи» и «Цифровой крепости». Но пока будущий Дэн Браун только собирается
            мыслями, предложим краткий конспект такого романа.<bR><br>

            Начиная с 2011 года основатель «ВКонтакте» Павел Дуров начинал покидать свое детище. Было все – фейковые
            вбросы в СМИ о продаже/не продажи его доли акций, силовики под дверями квартиры, какое-то придуманное ДТП о
            якобы наезде на инспектора ДПС и прочее, прочее, прочее. Весь этот прессинг, конечно, нервировал. И хотелось
            чисто по-человечески с кем-нибудь близким пообщаться. Таким близким человеком был его брат Николай. Но
            неожиданно, как потом писал Павел Дуров, он понял, что при современной технике ни один канал связи не может
            ему гарантировать конфиденциальность этого общения. Так родилась идея Telegram – создать мессенджер, который
            бы эту защиту гарантировал.<bR><br>

            В конце 2013 года Павел Дуров, наконец, продав свой пакет акций, смог сосредоточиться на новом своем детище
            – мессенджере Telegram. Правда, потом последовали претензии со стороны инвестиционного фонда United Capital
            Partners, владевшего на то время 48% акциями «ВКонтакте», что Дуров, будучи Генеральным директором этой
            социальной сети, использовал свое служебное положение для развития собственного проекта. Тот, разумеется,
            все отрицал. Но факт остаётся фактом, костяк команды, создавшей Telegram, являются наиболее талантливые и
            сейчас уже бывшие сотрудники «ВКонтакте». Олимпиадники, как их называет сам Дуров, то есть победители
            олимпиад по математике и программированию.
